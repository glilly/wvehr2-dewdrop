MIM Q:$D(DTOUT)!$D(DUOUT)
 S MCFILE1=MCFILE,X="MAGMIM" X ^%ZOSF("TEST")
 I '$T W !,"Imaging Package not available or you are not on a work station!"
 E  D ^MAGMIM
 S MCFILE=MCFILE1 Q
DISP Q:$D(DTOUT)!$D(DUOUT)
 S MCFILE1=MCFILE,X="MAGDISP" X ^%ZOSF("TEST") D:$T ^MAGDISP S MCFILE=MCFILE1 Q
EN1 Q:$D(DTOUT)!$D(DUOUT)
 S MCFILE1=MCFILE,X="MAGMIM" X ^%ZOSF("TEST") D:$T EN1^MAGMIM S MCFILE=MCFILE1 Q
SUM Q:$D(DTOUT)!$D(DUOUT)
 S MCFILE1=MCFILE,X="MAGSUM" X ^%ZOSF("TEST") D:$T ^MAGSUM S MCFILE=MCFILE1 Q
