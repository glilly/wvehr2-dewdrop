 S (YSLFT,T)=0 K Y D ENDTM^YSUTL S A=10,YSN="",B="TESTS" W @IOF D HD
F ;
 S (F,F1)=0,YSN=$O(^YTT(601,"ATN","T",YSN)) G:YSN="" H S T=$O(^(YSN,0))
 S X=^YTT(601,T,0) G:$P(X,U,13)="N" F S T1=$P(X,U) S:$P(X,U,14)="N" F=1 S:$P(X,U,3)["Y" F1=1 D TITLE
