SRONAN ;B'HAM ISC/MAM - ANNUAL REPORT FOR NON-O.R. PROCEDURES ; [ 09/22/98  11:34 AM ]
 ;;3.0; Surgery ;**77,50**;24 Jun 93
 S SRSOUT=0 W @IOF,!,"Annual Report of Non-O.R. Procedures",!
DATE D DATE^SROUTL(.SRSD,.SRED,.SRSOUT) G:SRSOUT END
 W ! K IOP,%ZIS,POP,IO("Q") S %ZIS("A")="Print the report on which Device: ",%ZIS="QM" D ^%ZIS I POP S SRSOUT=1 G END
 I $D(IO("Q")) K IO("Q") S ZTDESC="ANNUAL REPORT OF NON-O.R. PROCEDURES",ZTRTN="EN^SRONAN",(ZTSAVE("SRSD"),ZTSAVE("SRED"),ZTSAVE("SRSITE*"))="" D ^%ZTLOAD S SRSOUT=1 G END
EN ; entry when queued
 U IO N SRFRTO S Y=SRSD X ^DD("DD") S SRFRTO="FROM: "_Y_"  TO: ",Y=SRED X ^DD("DD") S SRFRTO=SRFRTO_Y,SRSD=SRSD-.0001,SRED=SRED+.9999,SRINST=SRSITE("SITE")
 D ^SRONAN1
 W:$E(IOST)="P" @IOF I $D(ZTQUEUED) K ^TMP("SR",$J) Q:$G(ZTSTOP)  S ZTREQ="@" Q
END D ^%ZISC
 I $E(IOST)'="P",'SRSOUT W !!,"Press RETURN to continue  " R X:DTIME
 D ^SRSKILL W @IOF
 Q
