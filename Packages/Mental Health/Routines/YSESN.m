YSESN ;SLC/DCM-ROUTINE TO PROCESS A JUMP TO NEW A ALGORITHM ; 10/18/88  16:55 ;
 ;;5.01;MENTAL HEALTH;;Dec 30, 1994
 ;DECISION EXPERT SYSTEM (VERSION 1.0) FOR MENTAL HEALTH PACKAGE - DWIGHT MCDANIEL / REGION 5 ISC, SLC
 ;
 ; Called by routine YSESD
E G EN:ESP?1"<".E1">",EN:ESP?1"[".E1"]",EN1:ESP?.E1"<".E1"[".E1"]"1">",EN2:ESP?.E1"{".E1"<".E1"[".E1"]"1">"1"}",EN2:ESP?1"{".E1"<".E1">"1"}"
PR W !!!?TB,"<< ",A5ALG_" ALGORITHM >>" Q
EN S ESDBP=A5AS(1)_GN_",1,",ESDBP1=ESDBP_"""B"",",A5ALG=$P($P(ESP,"<",2),">")
 S GN1=$O(@(ESDBP1_Q_A5ALG_Q_",0)")) I 'GN1 Q
 S A5AND=@(ESDBP_GN1_",1)"),ESDBP=ESDBP_GN1_",2,",ESDBP1=ESDBP_"""B"",",GN2=$O(@(ESDBP1_Q_A5AND_Q_",0)")) I GN2<1 S GN1=-1
 D:GN1>0 PR Q
 Q
EN1 S ESDBP=A5AS(1)_GN_",1,",ESDBP1=ESDBP_"""B"",",A5ALG=$P($P(ESP,"<",2),">"),A5AND=$P($P(A5ALG,"[",2),"]"),A5ALG=$P(A5ALG,"[")
 S GN1=$O(@(ESDBP1_Q_A5ALG_Q_",0)")) I GN1<1 Q
 S ESDBP=ESDBP_GN1_",2,",ESDBP1=ESDBP_"""B"",",GN2=$O(@(ESDBP1_Q_A5AND_Q_",0)")) I GN2'>0 S GN1=-1
 D:GN1>0 PR Q
 Q
EN2 S ESDBP=A5AS(1),ESDBP1=ESDBP_"""B"",",A5ASYS=$P($P(ESP,"{",2),"<"),GN=$O(@(ESDBP1_Q_A5ASYS_Q_",0)")) I GN'>0 S GN1=-1 Q
 I $P($P(ESP,"{",2),"}")?.E1"<".E1"[".E1"]"1">" D EN1 Q
 D EN Q
