 D HSKEEP G QUIT:$G(NUROUT)
 W ! D EN2^NURSAGSP G QUIT:$G(NUROUT)
 D EN1^NURSAGP0 G QUIT:$G(NUROUT)
 D DEV G:POP!($D(ZTSK)) QUIT
START1 ;
 K ^TMP($J),^TMP("NURA",$J) U IO S (NMCNT,NFCNT,NURPAGE,NURSW1,NURQUIT)=0,NURSSP="",$P(NURSSP," ",132)=" "
 S NURSTIL="GENDER BY SERVICE POSITION",NURSTIL1=$E(NURSSP,1,10)_"SERVICE",NURSTIL2="NO.       POSITION     NAME"_$E(NURSSP,1,22)_"GENDER"
 S NURROU=14,NRPT=4,NURSLEV=4 W ! D EN2^NURAAGS0,CHK G:$G(NUROUT) QUIT
 D PRINT^NURAGEN G:$G(NX)="^" QUIT
 S NURSORT(1)=0,NURSORT(2)=2,NURSORT(4)="NL(2)",NURSORT(3)="NL1(1)" D GENDER^NURAGEN2,TOTALS
 G QUIT
DEV ;
 S ZTDESC=$S(ANS2=1:"Gender Report by Service Category",1:"Gender Report by Service Position")
 W ! S ZTRTN=$S(ANS2=1:"START",1:"START1")_"^NURA6E1" D EN7^NURSUT0
 Q
QUIT K ^TMP("NURA",$J),^TMP($J) D CLOSE^NURSUT1,^NURAKILL
 Q
CHK D NODATA^NURAGEN
 Q
