 S DR="656REASON FOR SECOND COURSE OF THERAPY" D ^DIE G:$D(Y) PRTC
ST I $D(^ONCO(165.5,ONCONUM,4,0)) S SCTIEN=0 F  S SCTIEN=$O(^ONCO(165.5,ONCONUM,4,SCTIEN)) Q:SCTIEN'>0  D SS Q:$D(Y)
