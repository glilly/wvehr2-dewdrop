               NEW I
               FOR I=10:1:10 DO
               . WRITE !,"Working on table #",I
               . DO INST1("T"_I)
               QUIT
               ;
INST1(LABL)     ;
               ;"Purpose: to load up 1 table
               ;"Input: LABL -- should be T1 .. T14, for adding table.
               ;"Results: none
               NEW DONE SET DONE=0
               NEW DATA,SUBIEN
               NEW IEN SET IEN=0
               NEW OFFSET
               FOR OFFSET=0:1:9999 QUIT:DONE  DO
               . NEW S SET S=$TEXT(@LABL+OFFSET)
               . IF S'[";;" SET DONE=1 QUIT
               . SET S=$PIECE(S,";;""",2)
               . IF $PIECE(S,"^",1)="Sex" QUIT
               . IF S["=" DO  QUIT
               . . NEW FLD SET FLD=$PIECE(S,"=",1)
               . . NEW VAL SET VAL=$PIECE(S,"=",2,99)
               . . SET DATA(FLD)=VAL
               . IF IEN=0 DO
               . . IF $DATA(DATA) DO
               . . SET IEN=$$ENSUREC(.DATA)
               . SET SUBIEN=$$ENSSUBRC(IEN,S)
               . IF SUBIEN'>0 WRITE !,"ERROR ADDING: ",S,!
               QUIT
               ;
ENSUREC(DATA)   ;
               ;"Purpose: Create record for array if it doesn't exist, or return prior
               ;"         record if existing
               ;"Input: DATA -- PASS BY REFERENCE.  Format  DATA(Fld)=Value
               ;"Results: IEN of record, or -1 if error
               NEW TMGFDA,TMGIEN,TMGMSG
               NEW RESULT SET RESULT=-1
               NEW DIC,X,Y
               SET DIC=22713,DIC(0)="M"
               SET X=$GET(DATA(.01))
               IF X="" WRITE "?? NO .01 FIELD" GOTO ENSDN
               DO ^DIC
               SET RESULT=+Y
               IF Y>0 GOTO ENSDN
               NEW FLD SET FLD=""
               FOR  SET FLD=$ORDER(DATA(FLD)) QUIT:(FLD="")  DO
               . SET TMGFDA(22713,"+1,",FLD)=$GET(DATA(FLD))
               IF $DATA(TMGFDA)=0 GOTO ENSDN
               DO UPDATE^DIE("E","TMGFDA","TMGIEN","TMGMSG")
               IF $DATA(TMGMSG("DIERR")) DO SHOWDIERR^TMGGRCU(.TMGMSG) GOTO ENSDN
               SET RESULT=+$GET(TMGIEN(1))
ENSDN     QUIT RESULT
               ;
ENSSUBRC(PARENTIEN,DATA)        ;
               ;"Purpose: Ensure data filed into subfile
               ;"Input: PARENTIEN -- the is the IEN of the record that data is added into
               ;"       DATA -- A string in format of Sex^Agemos^L^M^S
               ;"Result : Returns subIEN, or -1 if error.
               NEW RESULT SET RESULT=-1
               SET PARENTIEN=+$GET(PARENTIEN) IF PARENTIEN'>0 GOTO ENS2DN
               SET DATA=$GET(DATA) IF DATA="" GOTO ENS2DN
               NEW INVAL SET INVAL=$PIECE(DATA,"^",2) IF INVAL="" GOTO ENS2DN
               NEW GENDER SET GENDER=$PIECE(DATA,"^",1) IF GENDER="" GOTO ENS2DN
               NEW L SET L=$PIECE(DATA,"^",3) IF L="" GOTO ENS2DN
               NEW M SET M=$PIECE(DATA,"^",4) IF M="" GOTO ENS2DN
               NEW S SET S=$PIECE(DATA,"^",5) IF S="" GOTO ENS2DN
               NEW FOUND SET FOUND=0
               NEW I SET I=0
               FOR  SET I=$ORDER(^TMG(22713,PARENTIEN,"D","B",INVAL,I)) QUIT:(+I'>0)!FOUND  DO
               . NEW NODE SET NODE=$GET(^TMG(22713,PARENTIEN,"D",I,0)) QUIT:NODE=""
               . IF $PIECE(NODE,"^",2)=GENDER SET FOUND=I QUIT
               IF FOUND>0 SET RESULT=FOUND GOTO ENS2DN
               NEW TMGIENS SET TMGIENS="+1,"_PARENTIEN_","
               NEW TMGFDA,TMGIEN,TMGMSG
               SET TMGFDA(22713.02,TMGIENS,.01)=INVAL
               SET TMGFDA(22713.02,TMGIENS,.02)=GENDER
               SET TMGFDA(22713.02,TMGIENS,.03)=L
               SET TMGFDA(22713.02,TMGIENS,.04)=M
               SET TMGFDA(22713.02,TMGIENS,.05)=S
               DO UPDATE^DIE("E","TMGFDA","TMGIEN","TMGMSG")
               IF $DATA(TMGMSG("DIERR")) DO SHOWDIERR^TMGGRCU(.TMGMSG) GOTO ENS2DN
               SET RESULT=+$GET(TMGIEN(1))
ENS2DN   QUIT RESULT
