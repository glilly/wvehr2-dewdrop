 E  W ?15,N G F
H ;
 S T=0,A=5,YSN="",B="INTERVIEWS" I $Y+8>IOSL D CK G:YSLFT FIN G G
 W !! D HD
G ;
 S YSN=$O(^YTT(601,"ATN","I",YSN)) G:YSN="" FIN S T=$O(^(YSN,0)),X=^YTT(601,T,0) G:$P(X,U,13)="N" G S T1=$P(X,U) D TITLE
