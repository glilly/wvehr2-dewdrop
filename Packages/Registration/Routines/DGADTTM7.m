 ;;5.3;REGISTRATION;**665**;Aug 13,1993
 ;
 ; This routine adds the new style cross-reference ADTTM4 which triggers
 ; an update for the DESIGNEE CHANGE DATE/TIME field (#.33012) in
 ; the PATIENT File #2.
 ;
 N DGENXR,DGENRES,DGENOUT
 S DGENXR("FILE")=2
 S DGENXR("NAME")="ADTTM7"
 S DGENXR("TYPE")="MU"
 S DGENXR("USE")="A"
 S DGENXR("EXECUTION")="R"
 S DGENXR("ACTIVITY")=""
 S DGENXR("SHORT DESCR")="DESIGNEE Cross-Reference"
 S DGENXR("DESCR",1)="This cross-reference will update the DESIGNEE CHANGE DATE/TIME field when"
 S DGENXR("DESCR",2)="the Designee data changes for a patient."
 S DGENXR("SET")="D DESIG^DGDDDTTM"
 S DGENXR("KILL")="D DESIG^DGDDDTTM"
 S DGENXR("VAL",1)=.341
 S DGENXR("VAL",1,"COLLATION")="F"
 S DGENXR("VAL",2)=.342
 S DGENXR("VAL",2,"COLLATION")="F"
 S DGENXR("VAL",3)=.343
 S DGENXR("VAL",3,"COLLATION")="F"
 S DGENXR("VAL",4)=.344
 S DGENXR("VAL",4,"COLLATION")="F"
 S DGENXR("VAL",5)=.345
 S DGENXR("VAL",5,"COLLATION")="F"
 S DGENXR("VAL",6)=.346
 S DGENXR("VAL",6,"COLLATION")="F"
 S DGENXR("VAL",7)=.347
 S DGENXR("VAL",7,"COLLATION")="F"
 S DGENXR("VAL",8)=.348
 S DGENXR("VAL",8,"COLLATION")="F"
 S DGENXR("VAL",9)=.3405
 S DGENXR("VAL",9,"COLLATION")="F"
 S DGENXR("VAL",10)=.2202
 S DGENXR("VAL",10,"COLLATION")="F"
 D CREIXN^DDMOD(.DGENXR,"W",.DGENRES,"DGENOUT")
 Q
