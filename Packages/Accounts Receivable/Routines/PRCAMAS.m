INCOMPL ;list of bill with 'Bill Incomplete' status
 S PRCAP("S")=$P($G(^IBE(350.9,1,1)),"^",14)
 S PRCAHDR="INCOMPLETE BILLS",PRCASORT="@INTERNAL(SERVICE),@CURRENT STATUS:STATUS NUMBER,@DATE BILL PREPARED,DEBTOR"
 S PRCATEMP="[PRCAR MAS REPORT]",PRCAFT=PRCAP("S")_",201,@,@",PRCALAST=PRCAP("S")_",201,,",DIC="^PRCA(430," D PRINT^PRCAREPT,END^PRCAREPT K PRCAS,PRCAP("S") Q
SUSP ;list of bills with 'SUSPENDED' status.
 Q  ;OBSOLETE 4.0
COMPL ;list of bills passed to Fiscal successfully other than 3rd party bills.
 S PRCA("DATE")="DATE BILL PREPARED" D ASKDT^PRCAQUE G:(PRCADT1="")!(PRCADT2="") END
 S PRCAMAS=$P($G(^IBE(350.9,1,1)),"^",14),PRCAFT=PRCADT1_","_PRCAMAS_",C,100",PRCALAST=PRCADT2_","_PRCAMAS_",P,199",PRCAHDR="MAS OTHER COMPLETED BILLS"
 S PRCATEMP="[PRCAR MAS REPORT]",PRCASORT="@DATE BILL PREPARED,@INTERNAL(SERVICE),@CATEGORY:INTERNAL(TYPE),@CURRENT STATUS:STATUS NUMBER"
 S DIC="^PRCA(430," D PRINT^PRCAREPT,END^PRCAREPT K PRCAMAS Q
END K PRCA,PRCADT1,PRCADT2 Q
