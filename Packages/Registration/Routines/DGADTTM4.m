DGADTTM4 ;ALB/RMM-CREATE NEW-STYLE XREF ;1:35 PM  24 May 2005
 ;;5.3;REGISTRATION;**665**;Aug 13,1993
 ;
 ; This routine adds the new style cross-reference ADTTM4 which triggers
 ; an update for the SECONDARY NOK CHANGE DATE/TIME field (#.211012) in
 ; the PATIENT File #2.
 ;
 N DGENXR,DGENRES,DGENOUT
 S DGENXR("FILE")=2
 S DGENXR("NAME")="ADTTM4"
 S DGENXR("TYPE")="MU"
 S DGENXR("USE")="A"
 S DGENXR("EXECUTION")="R"
 S DGENXR("ACTIVITY")=""
 S DGENXR("SHORT DESCR")="SECONDARY NOK Cross-Reference"
 S DGENXR("DESCR",1)="This cross-reference will update the SECONDARY NOK CHANGE DATE/TIME field"
 S DGENXR("DESCR",2)="when the Secondary Next of Kin data changes for a patient."
 S DGENXR("SET")="D SNOK^DGDDDTTM"
 S DGENXR("KILL")="D SNOK^DGDDDTTM"
 S DGENXR("VAL",1)=.2191
 S DGENXR("VAL",1,"COLLATION")="F"
 S DGENXR("VAL",2)=.2192
 S DGENXR("VAL",2,"COLLATION")="F"
 S DGENXR("VAL",3)=.2193
 S DGENXR("VAL",3,"COLLATION")="F"
 S DGENXR("VAL",4)=.2194
 S DGENXR("VAL",4,"COLLATION")="F"
 S DGENXR("VAL",5)=.2195
 S DGENXR("VAL",5,"COLLATION")="F"
 S DGENXR("VAL",6)=.2196
 S DGENXR("VAL",6,"COLLATION")="F"
 S DGENXR("VAL",7)=.2197
 S DGENXR("VAL",7,"COLLATION")="F"
 S DGENXR("VAL",8)=.2198
 S DGENXR("VAL",8,"COLLATION")="F"
 S DGENXR("VAL",9)=.21925
 S DGENXR("VAL",9,"COLLATION")="F"
 S DGENXR("VAL",10)=.2203
 S DGENXR("VAL",10,"COLLATION")="F"
 D CREIXN^DDMOD(.DGENXR,"W",.DGENRES,"DGENOUT")
 Q
