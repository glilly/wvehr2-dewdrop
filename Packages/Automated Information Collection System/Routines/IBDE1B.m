 N COUNT,LINE,QUIT
 S (QUIT,COUNT)=0
 W @IOF
 F  S LINE=$P($T(LINES+COUNT),";;",2) Q:QUIT!(LINE="")  D
 .S COUNT=COUNT+1
 .W !,LINE
 .I $Y>(IOSL-5) W !! D PAUSE
 I 'QUIT W !! D PAUSE
 Q QUIT
PAUSE ;
 K DIR S DIR(0)="E" D ^DIR K DIR S QUIT=$S(+Y:0,1:1)
 W:'QUIT @IOF
 Q
