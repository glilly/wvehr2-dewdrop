 W !?5,T1,?15,N G G
FIN ;
 K A,B,F,J,I0,N,P,T,T1,X,YSN Q
CK ;
 D:IOST?1"C-".E WAIT^YSUTL Q:YSLFT  W @IOF
HD ;
 W !!?A,"LIST OF CURRENT ",B,",  ",YSDT(1),!!
 Q
