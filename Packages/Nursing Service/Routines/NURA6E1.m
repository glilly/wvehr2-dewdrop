 D EN3^NURSAGSP G QUIT:$G(NUROUT)
 D EN1^NURSAGP0 G QUIT:$G(NUROUT)
 D DEV G:POP!($D(ZTSK)) QUIT
START ;
 K ^TMP($J),^TMP("NURA",$J) U IO S (NURPAGE,NURSW1,NURQUIT,NMCNT,NFCNT)=0,NURSSP="",$P(NURSSP," ",132)=" "
