MCENDIQ1 ;WISC/MLH-GET EXTERNAL FIELD VALUE ;5/4/92  09:54
 ;;2.3;Medicine;;09/13/1996
VALUE(MCFL,MCRECNO,MCFLDNO) ;    get field value
 N DA,DIC,DIQ,DR,MCY
 K ^UTILITY("DIQ1",$J)
 S DIC=MCFL,DR=MCFLDNO,DA=MCRECNO,DIQ(0)="E"
 D EN^DIQ1
 S MCY=$G(^UTILITY("DIQ1",$J,DIC,DA,DR,"E"))
 K ^UTILITY("DIQ1",$J)
 QUIT MCY
