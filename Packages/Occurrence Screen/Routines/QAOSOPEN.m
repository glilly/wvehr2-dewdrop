 W !!?11,"Reopening a record will delete all final disposition data",*7
 S QALIMIT="I $P(^(0),""^"",11)>0",QAOSPROG="DFN^QAOSOPEN" D EN2^QAOEDT
EXIT ;
 K %,DA,DIC,DIE,DR,QAUDIT,QAOSWHAT,Y,QAWHO,QAOSD0,%W,%Y,%Y1,C,D0,DI,DIG
 K DIH,DIU,DIV,DIW,DQ,I,QA,QACLOSE,QAHOLD,QAI,QALINE,QAOSWRD,QASCREEN
