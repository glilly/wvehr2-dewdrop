TEMP ; from FSCFORM, FSCRPTS
 N CALLREF,FLDS
 I $L($G(OK)),OK=0 Q
 S CALLREF=$P(^FSCD("CALL",CALLNUM,0),U),FLDS=FSCSTYLE("T")
 D FM(CALLREF,FLDS)
 Q
 ;
FM(CALLREF,FLDS) ;
 N DIC,L,FR,TO,DHD,BY,DIOBEG,DIOEND,DHIT K DIC
 S DIC="^FSCD(""CALL"",",L=0,BY=".01",DHD="@@",(FR,TO)=CALLREF,IOP="0",FLDS=$S($L($G(FLDS)):FLDS,1:"[CAPTIONED]")
 D EN1^DIP
 D PAUSE^FSCU(.OK) I 'OK Q
 Q
