YSJOB ;SLC/TGA,ALB/ASF-JOB BANK ; 2/22/89  13:18 ;
 ;;5.01;MENTAL HEALTH;;Dec 30, 1994
 ;
IN ; Called from MENU option YSJOBINQ
 ;
 ;INQUIRE JOBS
 W @IOF
IN1 ;
 S DIC="^YSG(""JOB"",",DIC(0)="AEQ" D ^DIC Q:Y'>0  S DA=+Y,DR="0:10" D EN^DIQ G IN1
 ;
PRT ; Called from MENU option YSJOBLIST
 ;PRINT JOB LIST
 W @IOF S DIC="^YSG(""JOB"",",FLDS="[YSJOB]",BY="TITLE;S1;C30",L=0 D EN1^DIP
 Q
 ;
UP ; Called from MENU option YSJOBUPDATE
 ;UPDATE JOB BANK
 W @IOF
UP1 ;
 W ! S DIC="^YSG(""JOB"",",DIC(0)="AEQLMZ",DLAYGO=624,DIC("W")="W ?44,$E($P(^(0),U,6),1,30)" D ^DIC Q:Y'>0
 S DA=+Y,DIE=DIC,DR=".01:20" L +^YSG("JOB",DA) D ^DIE L -^YSG("JOB",DA)
 S YSTOUT=$D(DTOUT) Q:YSTOUT
 G UP1
