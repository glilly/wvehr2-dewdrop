 N DVBI,DVBJ,DVBREF,DD,DIC,DO,X
 I '$D(DUZ)!'$D(U) W !!,"<<PROGRAMMER NOT DEFINED>>",! Q
 ;
DVBNEW ;add new codes
 W !!,"*** Updating DISABILITY CONDITION file (#31)",!?4,"per VA circular 21-95-2, dated Feb. 1, 1995",!
 ;
 F DVBI=1:1 S DVBJ=$P($T(NEWCODE+DVBI),";;",2) Q:DVBJ["$EXIT"  D
 .S DVBREF=+DVBJ
 .I '$D(^DIC(31,"C",DVBREF)) D
 ..K DD,DO
 ..S DIC="^DIC(31,",DIC(0)="L",DIC("DR")="2////"_DVBREF,X=$P(DVBJ,"^",2)
 ..D FILE^DICN
 ..W !,DVBREF,?10,X
 W !!,"  << DONE >>",!
 Q
