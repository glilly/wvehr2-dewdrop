DGADTTM1 ;ALB/RMM-CREATE NEW-STYLE XREF ;12:19 PM  24 May 2005
 ;;5.3;REGISTRATION;**665**;Aug 13,1993
 ;
 ; This routine adds the new style cross-reference ADTTM1 which triggers
 ; an update for the TEMPORARY ADDRESS CHANGE DT/TM field (#.12113) in
 ; the PATIENT File #2.
 ;
 N DGENXR,DGENRES,DGENOUT
 S DGENXR("FILE")=2
 S DGENXR("NAME")="ADTTM1"
 S DGENXR("TYPE")="MU"
 S DGENXR("USE")="A"
 S DGENXR("EXECUTION")="R"
 S DGENXR("ACTIVITY")=""
 S DGENXR("SHORT DESCR")="TEMPORARY ADDRESS Cross-Reference"
 S DGENXR("DESCR",1)="This cross-reference will update the TEMPORARY ADDRESS CHANGE DT/TM field"
 S DGENXR("DESCR",2)="when the Temporary Address data changes for a patient."
 S DGENXR("SET")="D TEMP^DGDDDTTM"
 S DGENXR("KILL")="D TEMP^DGDDDTTM"
 S DGENXR("VAL",1)=.1211
 S DGENXR("VAL",1,"COLLATION")="F"
 S DGENXR("VAL",2)=.1212
 S DGENXR("VAL",2,"COLLATION")="F"
 S DGENXR("VAL",3)=.1213
 S DGENXR("VAL",3,"COLLATION")="F"
 S DGENXR("VAL",4)=.1214
 S DGENXR("VAL",4,"COLLATION")="F"
 S DGENXR("VAL",5)=.1215
 S DGENXR("VAL",5,"COLLATION")="F"
 S DGENXR("VAL",6)=.1216
 S DGENXR("VAL",6,"COLLATION")="F"
 S DGENXR("VAL",7)=.1217
 S DGENXR("VAL",7,"COLLATION")="F"
 S DGENXR("VAL",8)=.1218
 S DGENXR("VAL",8,"COLLATION")="F"
 S DGENXR("VAL",9)=.12105
 S DGENXR("VAL",9,"COLLATION")="F"
 S DGENXR("VAL",10)=.12112
 S DGENXR("VAL",10,"COLLATION")="F"
 D CREIXN^DDMOD(.DGENXR,"W",.DGENRES,"DGENOUT")
 Q
