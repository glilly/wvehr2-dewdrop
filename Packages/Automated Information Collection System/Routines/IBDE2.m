DLTALL N FILE,NODE,COUNT
 S COUNT=0
 F  S FILE=$P($T(FILES+COUNT),";;",2) Q:FILE=""  D
 .S COUNT=COUNT+1
 .S NODE=$G(@FILE@(0))
 .S $P(NODE,"^",3)=0,$P(NODE,"^",4)=0
 .K @FILE
 .S @FILE@(0)=NODE
 S VALMBCK="R",VALMCNT=0,BLKCNT=0,FORMCNT=0
 K ^TMP("IBDF",$J,"IMP/EXP WS")
 I $G(SCREEN)="F" D HDR^IBDE
 I $G(SCREEN)="B" D HDR^IBDE3
 Q
