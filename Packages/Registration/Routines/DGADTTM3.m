DGADTTM3 ;ALB/RMM-CREATE NEW-STYLE XREF ;1:35 PM  24 May 2005
 ;;5.3;REGISTRATION;**665**;Aug 13,1993
 ;
 ; This routine adds the new style cross-reference ADTTM3 which triggers
 ; an update for the PRIMARY NOK CHANGE DATE/TIME field (#.21012) in
 ; the PATIENT File #2.
 ;
 N DGENXR,DGENRES,DGENOUT
 S DGENXR("FILE")=2
 S DGENXR("NAME")="ADTTM3"
 S DGENXR("TYPE")="MU"
 S DGENXR("USE")="A"
 S DGENXR("EXECUTION")="R"
 S DGENXR("ACTIVITY")=""
 S DGENXR("SHORT DESCR")="PRIMARY NOK Cross-Reference"
 S DGENXR("DESCR",1)="This cross-reference will update the PRIMARY NOK CHANGE DATE/TIME field"
 S DGENXR("DESCR",2)="when the Primary Next of Kin data changes for a patient."
 S DGENXR("SET")="D PNOK^DGDDDTTM"
 S DGENXR("KILL")="D PNOK^DGDDDTTM"
 S DGENXR("VAL",1)=.211
 S DGENXR("VAL",1,"COLLATION")="F"
 S DGENXR("VAL",2)=.212
 S DGENXR("VAL",2,"COLLATION")="F"
 S DGENXR("VAL",3)=.213
 S DGENXR("VAL",3,"COLLATION")="F"
 S DGENXR("VAL",4)=.214
 S DGENXR("VAL",4,"COLLATION")="F"
 S DGENXR("VAL",5)=.215
 S DGENXR("VAL",5,"COLLATION")="F"
 S DGENXR("VAL",6)=.216
 S DGENXR("VAL",6,"COLLATION")="F"
 S DGENXR("VAL",7)=.217
 S DGENXR("VAL",7,"COLLATION")="F"
 S DGENXR("VAL",8)=.218
 S DGENXR("VAL",8,"COLLATION")="F"
 S DGENXR("VAL",9)=.2125
 S DGENXR("VAL",9,"COLLATION")="F"
 S DGENXR("VAL",10)=.2207
 S DGENXR("VAL",10,"COLLATION")="F"
 D CREIXN^DDMOD(.DGENXR,"W",.DGENRES,"DGENOUT")
 Q
