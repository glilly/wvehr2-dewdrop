 S NURROU=13,NRPT=4,NURSLEV=3 W ! D EN1^NURAAGS0,CHK G:$G(NUROUT) QUIT
 D PRINT^NURAGEN G:$G(NX)="^" QUIT
 S NURSORT(1)=0,NURSORT(2)=2,NURSORT(3)="NL1",NURSORT(4)="NL(1)" D GENDER^NURAGEN2,TOTALS
 G QUIT
