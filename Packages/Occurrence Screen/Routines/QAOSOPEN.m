 S %=2 D YN^DICN S QAOSQUIT=$S(%=-1:1,1:0) Q:%=2!QAOSQUIT
 I '% W !!?10,"Please answer Y(es) or N(o)" G ASK
 S DIE="^QA(741,",DA=QAOSD0
 S DR="11///^S X=0;14///^S X=""@"";15///^S X=""@"";16///^S X=""@"""
 D ^DIE
 S QAUDIT("FILE")="741^27",QAUDIT("DA")=DA,QAUDIT("ACTION")="o"
 S QAUDIT("COMMENT")="REOPEN A CLOSED/DELETED RECORD" D ^QAQAUDIT
 W !!?5,"*** RECORD HAS BEEN REOPENED ***",*7,!
 Q
